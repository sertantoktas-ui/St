@analytics_bp.route('/requests/sla-compliance', methods=['GET'])
@jwt_required()
def sla_compliance():
    """Get SLA compliance metrics"""
    now = datetime.utcnow()

    completed_on_time = ServiceRequest.query.filter(
        ServiceRequest.status == 'completed',
        ServiceRequest.actual_end_time <= ServiceRequest.sla_end_time
    ).count()

    total_with_sla = ServiceRequest.query.filter(
        ServiceRequest.sla_end_time != None
    ).count()

    compliance_rate = (completed_on_time / total_with_sla * 100) if total_with_sla > 0 else 0

    return jsonify({
        'total_with_sla': total_with_sla,
        'completed_on_time': completed_on_time,
        'compliance_rate': round(compliance_rate, 2)
    }), 200


@analytics_bp.route('/reports/monthly', methods=['GET'])
@jwt_required()
def monthly_report():
    """Get monthly performance report"""
    month = request.args.get('month', default=datetime.utcnow().month, type=int)
    year = request.args.get('year', default=datetime.utcnow().year, type=int)

    start_date = datetime(year, month, 1)
    if month == 12:
        end_date = datetime(year + 1, 1, 1)
    else:
        end_date = datetime(year, month + 1, 1)

    # Service metrics
    requests_created = ServiceRequest.query.filter(
        ServiceRequest.created_at.between(start_date, end_date)
    ).count()

    requests_completed = ServiceRequest.query.filter(
        ServiceRequest.status == 'completed',
        ServiceRequest.actual_end_time.between(start_date, end_date)
    ).count()

    # Revenue metrics
    invoices = Invoice.query.filter(
        Invoice.issue_date.between(start_date, end_date),
        Invoice.status == 'paid'
    ).all()

    total_revenue = sum(inv.total_amount for inv in invoices)

    # Labor metrics
    service_logs = ServiceLog.query.filter(
        ServiceLog.start_time.between(start_date, end_date)
    ).all()

    total_hours = sum(log.duration_hours for log in service_logs if log.duration_hours)
    total_labor_cost = sum(log.labor_cost for log in service_logs if log.labor_cost)

    return jsonify({
        'period': f'{year}-{month:02d}',
        'requests': {
            'created': requests_created,
            'completed': requests_completed,
            'completion_rate': (requests_completed / requests_created * 100) if requests_created > 0 else 0
        },
        'revenue': {
            'total': total_revenue,
            'invoice_count': len(invoices)
        },
        'labor': {
            'total_hours': total_hours,
            'total_cost': total_labor_cost
        }
    }), 200


@analytics_bp.route('/health-check', methods=['GET'])
@jwt_required()
def system_health():
    """Get system health indicators"""
    now = datetime.utcnow()

    # Overdue requests
    overdue = ServiceRequest.query.filter(
        ServiceRequest.sla_end_time < now,
        ServiceRequest.status != 'completed'
    ).count()

    # Overdue invoices
    overdue_invoices = Invoice.query.filter(
        Invoice.due_date < now,
        Invoice.status != 'paid'
    ).count()

    # Low stock items
    from models import Inventory
    low_stock = Inventory.query.filter(
        Inventory.quantity < Inventory.min_quantity
    ).count()

    return jsonify({
        'overdue_requests': overdue,
        'overdue_invoices': overdue_invoices,
        'low_stock_items': low_stock,
        'system_status': 'healthy' if overdue < 10 and overdue_invoices < 5 else 'warning'
    }), 200
