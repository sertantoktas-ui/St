analytics_bp = Blueprint('analytics', __name__)


@analytics_bp.route('/dashboard', methods=['GET'])
@jwt_required()
def get_dashboard():
    """Get dashboard metrics"""
    now = datetime.utcnow()
    thirty_days_ago = now - timedelta(days=30)

    # Service request metrics
    total_requests = ServiceRequest.query.count()
    open_requests = ServiceRequest.query.filter_by(status='open').count()
    completed_requests = ServiceRequest.query.filter_by(status='completed').count()
    in_progress = ServiceRequest.query.filter_by(status='in_progress').count()

    # Revenue metrics
    paid_invoices = Invoice.query.filter_by(status='paid').all()
    total_revenue = sum(inv.total_amount for inv in paid_invoices)
    pending_revenue = sum(inv.total_amount for inv in Invoice.query.filter(
        Invoice.status.in_(['sent', 'overdue'])
    ).all())

    # Recent requests
    recent_requests = ServiceRequest.query.order_by(
        ServiceRequest.created_at.desc()
    ).limit(5).all()

    # Technician utilization
    active_technicians = Technician.query.filter_by(status='available').count()
    busy_technicians = Technician.query.filter_by(status='busy').count()

    return jsonify({
        'service_requests': {
            'total': total_requests,
            'open': open_requests,
            'in_progress': in_progress,
            'completed': completed_requests,
            'completion_rate': (completed_requests / total_requests * 100) if total_requests > 0 else 0
        },
        'revenue': {
            'total_received': total_revenue,
            'pending': pending_revenue,
            'total_potential': total_revenue + pending_revenue
        },
        'technicians': {
            'active': active_technicians,
            'busy': busy_technicians,
            'utilization_rate': (busy_technicians / (active_technicians + busy_technicians) * 100) if (active_technicians + busy_technicians) > 0 else 0
        },
        'recent_requests': [r.to_dict() for r in recent_requests]
    }), 200


@analytics_bp.route('/performance/technicians', methods=['GET'])
@jwt_required()
def technician_performance():
    """Get technician performance metrics"""
    technicians = Technician.query.all()

    performance = []
    for tech in technicians:
        total_assigned = len(tech.assignments)
        completed = len([r for r in tech.assignments if r.status == 'completed'])
        total_hours = sum(log.duration_hours for log in tech.service_logs if log.duration_hours)

        performance.append({
            'technician_id': tech.id,
            'name': tech.user.full_name,
            'specialization': tech.specialization,
            'rating': tech.rating,
            'total_assigned': total_assigned,
            'completed': completed,
            'completion_rate': (completed / total_assigned * 100) if total_assigned > 0 else 0,
            'total_hours': total_hours,
            'status': tech.status
        })

    return jsonify(performance), 200


